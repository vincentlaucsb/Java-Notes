class HelloWorld {
    /*
    Just like in C++, a Java program begins with a call to main()
     * public --> Other parts of this program (if there were any)
       can access this method
     * String args[] --> This program can take any command line 
       arguments as a string array (but it doesn't use them)
    */
    
    public static void main (String args[]) {
        System.out.println("Hello world!");
    }
    
}